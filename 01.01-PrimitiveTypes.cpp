#include <iostream>
using namespace std;

int main() {
    int n;
    float f;
    double d = 0.0;
    char c = '*';
    cout << "address of n: " << &n << endl;
    cout << "address of f: " << &f << endl;
    cout << "address of d: " << &d << endl;
    cout << "address of c: " << &c << endl;
    return 0;
}